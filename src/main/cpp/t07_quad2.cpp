//Даны действительные коэффициенты a, b, c. Решите уравнение ax2 + bx + c = 0 и выведите все его корни.
//
//Формат входных данных
//
//        Вводятся три действительных числа.
//Формат выходных данных
//        Если данное уравнение не имеет корней, выведите число 0. Если уравнение имеет один корень, выведите число 1, а затем этот корень. Если уравнение имеет два корня, выведите число 2, а затем два корня в порядке возрастания. Если уравнение имеет бесконечно много корней, выведите число 3.
//Sample Input:
//
//1
//-1
//-2
//Sample Output:
//
//2 -1 2

#include "t07_quad2.h"
#include <iostream>
#include <cmath>

using namespace std;

int t07_quad2() {
    double a, b, c;
    cin >> a >> b >> c;
    if (a == 0 && b == 0 && c == 0)
        cout << 3;
    else {

        if ((a == 0 && b == 0 && c != 0) || pow(b, 2) - 4 * a * c < 0)
            cout << 0;
        else if (pow(b, 2) - 4 * a * c == 0 || a == 0) {
            cout << 1 << ' ';
            pow(b, 2) - 4 * a * c == 0 ? cout << (-b) / 2 / a : cout << (-c) / b;
        }
        else {
            cout << 2 << ' ';
            if ((-b - sqrt(pow(b, 2) - 4 * a * c))/2 / a < (-b + sqrt(pow(b, 2) - 4 * a * c))/2 / a)
                cout << (-b - sqrt(pow(b, 2) - 4 * a * c))/2 / a << ' ' << (-b + sqrt(pow(b, 2) - 4 * a * c))/2 / a;
            else
                cout << (-b + sqrt(pow(b, 2) - 4 * a * c))/2 / a << ' ' << (-b - sqrt(pow(b, 2) - 4 * a * c))/2 / a;
        }
    }
};